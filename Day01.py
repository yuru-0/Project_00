"""
2. Python: 정보 추출 (문자열 슬라이싱)
웹 해킹이든 시스템 해킹이든, CTF의 첫걸음은 서버가 뱉어내는 텍스트(문자열)에서 원하는 정보만 칼같이 잘라내는 것이다.

상황: 서버 로그 파일에서 다음과 같은 에러 로그를 하나 확보했다.
log_data = "ERROR: [2026-05-09 10:18:51] Failed login attempt from IP: 192.168.1.100"

과제: 위 문자열 변수에서 파이썬의 문자열 처리 기법(슬라이싱, split() 등)을 활용하여 오직 192.168.1.100 이라는 IP 주소 부분만 추출해서 변수에 담고 출력하는 코드를 작성해라.

조건: 인덱스 번호를 직접 세어서 자르는 하드코딩(log_data[60:73] 같은 방식)은 금지한다. 로그의 길이가 달라져도 항상 'IP: ' 뒤의 주소를 가져올 수 있도록 논리를 구성해라.
"""

"""
슬리아싱 ( slicing ) : 문자열에서 특정 부분을 잘라내는 방법.
연속적인 객체들에 범위를 지정하여 원하는 부분을 추출하는 기법.

문자열 슬라이싱의 기본 문법 : 문자열[시작인덱스:끝인덱스:간격]
시작인덱스 : 슬라이싱이 시작되는 위치 (0부터 시작)
끝인덱스 : 슬라이싱이 끝나는 위치 (끝인덱스는 포함되지 않음)
간격 : 슬라이싱에서 요소를 건너뛰는 간격 (기본값은 1)
"""

"""
특정 문자열 뒤의 내용을 추출하는 방법 : 
1. split() 함수 사용 : 문자열을 특정 구분자로 나누어 리스트로 반환하는 함수.

text = "hello_world"
result = text.split('_')[1]
# '_'를 기준으로 문자열을 나누고, 두 번째 요소(인덱스 1)를 가져온다. result -> 'world'
print(result) # 'world' 출력

2. find() 함수 사용 : 문자열에서 특정 부분의 시작 인덱스를 반환하는 함수.

text = "hello_world"
idx = text.find('_') # '_'의 시작 인덱스를 찾는다. idx -> 5

if idx != -1:
    result = text[idx + 1:] # '_' 다음부터 문자열 끝까지 슬라이싱한다. result -> 'world'
    print(result) # 'world' 출력

3. partition() 함수 사용 : 문자열을 특정 구분자로 나누어 세 부분으로 반환하는 함수.

text = "hello_world"
_, _, result = text.partition('_')
# '_'를 기준으로 문자열을 나누고, 세 번째 요소를 가져온다. result -> 'world'
print(result) # 'world' 출력
"""

Log_address = input("로그 데이터 입력 : ") # 사용자로부터 로그 데이터를 입력받는다.
IP_address = Log_address.split('IP: ')[1] # 'IP: '를 기준으로 문자열을 나누고, 두 번째 요소를 가져온다.
print("추출된 IP 주소:", IP_address)