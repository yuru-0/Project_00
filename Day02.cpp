/*
1. C/C++: 포인터(Pointer) - 메모리 직접 조작
포인터는 C언어의 꽃이자, 해킹(메모리 변조)의 핵심이다. 주소를 '보는 것'을 넘어 주소를 통해 데이터를 '원격 조종'하는 법을 익힌다.

과제:

1. 정수형 변수 target을 선언하고 값 100을 넣어라.

2. target의 메모리 주소를 담을 포인터 변수 ptr을 선언하고 주소를 연결해라.

3. target이라는 변수명은 절대 사용하지 말고, 오직 포인터 ptr만을 이용하여 그 공간의 값을 999로 조작(변경)해라.

4. 마지막에 cout << target;을 출력했을 때 999가 나오면 성공이다.

핵심: 참조 연산자(*)의 역할을 완벽하게 이해해라.

질문: 변수명(target)을 직접 써서 값을 바꾸는 것과, 포인터(ptr)를 이용해 메모리 주소로 접근해 값을 바꾸는 것은 어떤 차이가 있을까? (어느 상황에서 포인터가 강력한 무기가 될지 고민해 봐라.)
*/

/*
포인터 : C의 자료형 중 하나로, 메모리의 주소를 저장하는 자료형.
참조 연산자(*) : 포인터 변수가 가리키는 메모리 주소에 저장된 값을 참조하거나 수정할 때 사용하는 연산자.
예를 들어, *ptr은 ptr이 가리키는 주소에 저장된 값을 의미한다.

& 연산자 : 변수의 주소를 얻을 때 사용하는 연산자.
예를 들어, &target은 target 변수의 메모리 주소를 반환한다.

역참조 연산자 : 포인터 변수가 가리키는 주소에 저장된 값을 참조하거나 수정할 때 사용하는 연산자.
예를 들어, *ptr = 999;는 ptr이 가리키는 주소에 저장된 값을 999로 변경한다.

포인터를 이용한 메모리 조작의 강력함 :
1. 직접 메모리 접근 : 포인터를 사용하면 변수명 없이도 메모리 주소를 통해 데이터를 조작할 수 있다.
이는 특히 배열, 구조체, 동적 메모리 할당 등에서 유용하다.
2. 함수 인자로 활용 : 포인터를 이용하면 함수에 변수의 주소를 전달하여, 함수 내에서 원본 데이터를 수정할 수 있다.
이는 값 복사보다 효율적이다.
3. 동적 메모리 관리 : 포인터를 사용하면 런타임에 필요한 만큼 메모리를 할당하고 해제할 수 있다.
4. 시스템 프로그래밍 : 운영체제, 드라이버, 임베디드 시스템 등 저수준 프로그래밍에서 포인터는 필수적이다.
5. 보안 취약점 : 포인터를 잘못 사용하면 버퍼 오버플로우, 메모리 누수, dangling pointer 등 심각한 보안 취약점이 발생할 수 있다.
따라서 포인터를 사용할 때는 항상 주의가 필요하다.
*/

#include <iostream>
using namespace std;

int main()
{
    int target = 100;
    int *ptr = &target; // target의 주소를 ptr에 저장

    cout << "Before: " << target << endl; // target의 초기값 출력 (100)

    *ptr = 999; // ptr이 가리키는 주소에 999를 저장

    cout << "After: " << target << endl; // target의 값이 999로 변경되어 출력됨
}