"""
2. Python: 암호학 기초 - 시저 암호(Caesar Cipher) 구현
CTF에 단골로 등장하는 기초 고전 암호다. 문자를 다른 문자로 치환하는 논리를 만든다.

과제: 알파벳 대문자로만 이루어진 문자열(예: "HAL")을 입력받아, 각 알파벳을 뒤로 딱 1칸씩 밀어서 출력하는 코드를 작성해라.
(H -> I, A -> B, L -> M. 결과는 "IBM"이 되어야 한다.)

힌트: 컴퓨터는 문자를 결국 숫자(아스키코드)로 기억한다. 문자를 숫자로 바꾸는 ord() 함수와, 숫자를 다시 문자로 바꾸는 chr() 함수를 찾아보고 활용해라.
for문을 이용해 문자열을 한 글자씩 분해해서 처리해야 할 거다.
"""

"""
ord() 함수 : 
하나의 문자를 입력받아 해당 문자의 아스키 코드 값을 반환하는 함수.
예를 들어, ord('A')는 65를 반환합니다.

chr() 함수 :
하나의 아스키 코드 값을 입력받아 해당 코드에 해당하는 문자를 반환하는 함수.
예를 들어, chr(65)는 'A'를 반환합니다.
"""

"""
List Comprehension :
기존 리스트를 기반으로 새로운 리스트를 간결하고 효율적으로 생성하는 문법.

기본 문법
[표현식 for 항목 in iterable_객체 if 조건문]

1. 기본형 : 각 항목에 연산을 적용하여 새로운 리스트 생성
# 0부터 4까지의 숫자에 각각 2를 곱한 리스트 생성
result = [x * 2 for x in range(5)]
# 결과: [0, 2, 4, 6, 8]

2. 조건문 포함 (필터링) : if문을 추가하여 특정 조건을 만족하는 항목만 포함
# 0부터 9까지의 숫자 중 짝수만 추출
result = [x for x in range(10) if x % 2 == 0]
# 결과: [0, 2, 4, 6, 8]

3. if-else 조건문 활용 : 조건에 따라 리스트에 담길 값을 다르게 지정
# 0부터 4까지의 숫자 중 짝수는 '짝수', 홀수는 '홀수'로 변환
result = ['짝수' if x % 2 == 0 else '홀수' for x in range(5)]
# 결과: ['짝수', '홀수', '짝수', '홀수', '짝수']

4. 중첩 for문 활용 : 여러 개의 반복문을 한 줄로 표현
# 두 리스트의 요소를 곱한 모든 조합 구하기
result = [x * y for x in [1, 2] for y in [10, 20]]
# 결과: [10, 20, 20, 40]
"""

text = "HAL"
ascii_values = [ord(char) for char in text]  # 각 문자를 아스키 코드로 변환

print(ascii_values)  # 아스키 코드 값 출력 [72, 65, 76] 출력, 확인 작업

# for문을 이용하여 각 문자를 아스키 코드로 변환 후 1을 더하고 다시 문자로 변환
for char in text:
    # 1. ord() 함수를 이용하여 문자를 아스키 코드로 변환 후 + 1
    ascii_value = ord(char) + 1
    # 2. chr() 함수를 이용하여 아스키 코드 값을 다시 문자로 변환
    new_char = chr(ascii_value)

    print(new_char, end="")  # 결과 출력 "IBM" 출력

# List Comprehension을 이용하여 각 문자를 아스키 코드로 변환 후 1을 더하고 다시 문자로 변환
new_text = "".join([chr(ord(char) + 1) for char in text])  # 각 문자를 아스키 코드로 변환 후 1을 더하고 다시 문자로 변환
print("\n" + new_text)  # 결과 출력 "IBM" 출력