"""
2. Python: 데이터 필터링
보안 분석가는 수많은 로그 중에서 의미 있는 데이터를 골라낼 줄 알아야 한다.

과제: 1부터 100 사이의 숫자 중, 3의 배수이면서 동시에 5의 배수인 숫자만 리스트에 담아 출력하는 코드를 작성해라.

핵심: for문과 if 조건문, 그리고 리스트의 append 기능을 활용해라.

질문: 이 로직을 '가장 적은 횟수의 비교'로 수행하려면 어떻게 해야 할까?
"""

"""
3. 사고력 훈련 (보고서)
위의 Python 과제 로직을 코드를 모르는 사람에게 설명한다고 가정하고, 수행 순서(Step 1, 2...)를 번호를 매겨 논리적으로 적어라. 최대한 간결하고 명확하게.

Step 1 : 1부터 100까지의 숫자를 하나씩 살펴본다. ( for 문을 사용하여 1부터 100까지 반복한다. )
Step 2 : 각 숫자가 3의 배수인지 확인한다. ( if 문을 사용하여 숫자가 3으로 나누어 떨어지는지 확인한다. )
Step 3 : 각 숫자가 5의 배수인지 확인한다. ( if 문을 사용하여 숫자가 5로 나누어 떨어지는지 확인한다. )
Step 4 : 숫자가 3의 배수이면서 동시에 5의 배수인지 확인한다. ( and 연산자를 사용하여 두 조건이 모두 참인지 확인한다. )
Step 5 : 조건을 만족하는 숫자를 리스트에 추가한다. ( append 함수를 사용하여 조건을 만족하는 숫자를 리스트에 추가한다. )
Step 6 : 모든 숫자를 확인한 후, 리스트에 담긴 숫자들을 출력한다. ( print 함수를 사용하여 리스트에 담긴 숫자들을 출력한다. )
"""

arr = [] # 결과를 저장할 빈 리스트를 만든다. ( 리스트는 여러 값을 저장할 수 있는 자료형이다. )

for i in range(1, 101): # 1부터 100까지의 숫자를 반복. for ( 변수 ) in range ( 시작값 , 끝값 + 1 , 간격 )
    if i % 3 == 0 and i % 5 == 0: # i가 3의 배수이면서 동시에 5의 배수인지 확인. if ( 조건1 ) and ( 조건2 )
        print(i)
        arr.append(i) # i가 조건을 만족할 때마다 arr 리스트에 i를 추가한다. arr.append ( 값 )
    
print(arr) # arr 리스트의 모든 요소를 출력한다. print ( 변수 )
    